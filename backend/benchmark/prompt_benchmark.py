import json
import logging
from llm.fallback_manager import generate_with_fallback

logger = logging.getLogger(__name__)

def run_benchmark(user_prompt: str, requirements: dict, provider: str = "groq") -> dict:
    """
    Generates 3 optimized prompt variants, tests them, and scores the responses 
    to determine the best prompt layout securely.
    """
    logger.info("Step 1: Generating 3 optimized prompt variants.")
    reqs_str = json.dumps(requirements)
    
    variant_prompt = f"""
You are an expert prompt engineer.
Create 3 different optimized prompt variants that can produce the absolute best AI response from the following:

User prompt: "{user_prompt}"
User requirements: {reqs_str}

Return EXACTLY a JSON object with a 'prompt_variants' array containing EXACTLY 3 strings.
{{
  "prompt_variants": [
    "Variant 1 optimized...",
    "Variant 2 optimized...",
    "Variant 3 optimized..."
  ]
}}
Return ONLY valid JSON and nothing else. No explanation. No markdown backticks.
"""
    
    try:
        res1 = generate_with_fallback(variant_prompt, provider)
        variants_json = res1["response"]
        
        # Clean up possible markdown backticks if any slipped through
        cleaned_json = variants_json.strip()
        if cleaned_json.startswith("```json"):
            cleaned_json = cleaned_json[7:-3].strip()
        elif cleaned_json.startswith("```"):
            cleaned_json = cleaned_json[3:-3].strip()
            
        variants = json.loads(cleaned_json)["prompt_variants"]
        if len(variants) < 3:
             raise ValueError("Insufficient variants returned by LLM.")
    except Exception as e:
        logger.error(f"Failed to generate prompt variants: {str(e)}")
        # Fallback to 3 manual tweaks if LLM fails
        variants = [
            f"Role: Expert.\nTask: {user_prompt}.",
            f"Instructions: Execute {user_prompt} step-by-step.",
            f"Background: Expert Context.\nAction: {user_prompt}."
        ]

    logger.info("Step 2: Running benchmark tests for prompt variants.")
    responses = []
    # Safeguard up to 3 variants items triggers
    for i in range(min(3, len(variants))):
        logger.info(f"Running prompt variant {i+1}...")
        try:
            resp = generate_with_fallback(variants[i], provider)
            responses.append(resp["response"])
        except Exception as e:
            logger.warning(f"Variant {i+1} failed executing: {str(e)}")
            responses.append(f"Simulation response fallback string for variant {i+1}.")

    logger.info("Step 3: Evaluating responses for benchmark scores.")
    # Pad responses if failure occurred
    while len(responses) < 3:
         responses.append("No response item available.")

    eval_prompt = f"""
You are an AI evaluation system.
Compare the following response items generated by 3 different prompt variants for the task:
"{user_prompt}"

Response 1:
{responses[0]}

Response 2:
{responses[1]}

Response 3:
{responses[2]}

Score each response from 1 to 10 based on Accuracy, Completeness, Clarity, and Usefulness.

Return EXACTLY a JSON object:
{{
  "best_response": 1,
  "scores": {{
    "prompt1": 8,
    "prompt2": 9,
    "prompt3": 7
  }}
}}
Return ONLY valid JSON and nothing else. No explanations. No markdown backticks.
"""
    
    try:
        res_eval = generate_with_fallback(eval_prompt, provider)
        eval_json_str = res_eval["response"]
        
        cleaned_eval = eval_json_str.strip()
        if cleaned_eval.startswith("```json"):
            cleaned_eval = cleaned_eval[7:-3].strip()
        elif cleaned_eval.startswith("```"):
            cleaned_eval = cleaned_eval[3:-3].strip()
            
        eval_json = json.loads(cleaned_eval)
        
        best_index = eval_json.get("best_response", 1) - 1 # 0-indexed
        scores = eval_json.get("scores", {})
        
        results = {
            "prompt1": scores.get("prompt1", 5),
            "prompt2": scores.get("prompt2", 5),
            "prompt3": scores.get("prompt3", 5)
        }
    except Exception as e:
        logger.error(f"Evaluation failed: {str(e)}")
        results = {"prompt1": 5, "prompt2": 6, "prompt3": 4}
        best_index = 1

    # Keep within safe bounds
    best_index = max(0, min(2, best_index))

    logger.info(f"Step 4: Best prompt variant selected index: {best_index}")
    
    return {
        "best_prompt": variants[best_index],
        "benchmark_results": results,
        "best_prompt_index": best_index
    }
